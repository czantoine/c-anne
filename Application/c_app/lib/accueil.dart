class _Accueil_Screen extends State<Accueil_Screen> {
  int _pageIndex = 0;
  PageController _pageController;

  List<Widget> tabPages = [
    Home(), //screen1
    Profile(), //screen2
    MenuOptionsScreen(), //screen4
  ];

  @override
  void initState(){
    super.initState();
    _pageController = PageController(initialPage: _pageIndex);
  }


  @override
  Widget build(BuildContext context) {
    return Scaffold(
      bottomNavigationBar: BottomNavigationBar(
        currentIndex: _pageIndex,
        onTap: onTabTapped,
        backgroundColor: Colors.white,
        items: <BottomNavigationBarItem>[
          BottomNavigationBarItem( icon: Icon(Icons.home), title: Text("Home")),
          BottomNavigationBarItem(icon: Icon(Icons.assessment), title: Text("Graph")),
          BottomNavigationBarItem(icon: Icon(Icons.settings), title: Text("Options")),
        ],
        selectedItemColor: HexColor('#6C63FF'),
        unselectedItemColor: Colors.grey,

      ),
      body: PageView(
        children: tabPages,
        onPageChanged: onPageChanged,
        controller: _pageController,
      ),
    );
  }
  void onPageChanged(int page) {
    setState(() {
      this._pageIndex = page;
    });
  }

  void onTabTapped(int index) {
    this._pageController.animateToPage(index,duration: const Duration(milliseconds: 500),curve: Curves.easeInOut);
  }
}

